export function membershipApprovedMessage(name: string, membershipNumber: string) {
  return `Congratulations ${name}. Your FHBF membership has been approved. Your membership number is ${membershipNumber}.`;
}

export function villageStandAllocatedMessage(name: string, standNumber: string) {
  return `Hello ${name}, your Faith Heights Business Village stand has been allocated: ${standNumber}.`;
}
